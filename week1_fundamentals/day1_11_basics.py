#Problem 8:A force of 15000N is applied on a circular cross-section with radius = 0.04m. Calculate area (π × r²) and stress (force/area).
r = 0.04 #Radius of circle
area = 3.14*(r**2) #Area of circle 
print("The area of the circular cross-section is: ", area)
force = 15000 #In Newton
stress = force/area
print("The stress acting on the circular cross-section is: ", stress)