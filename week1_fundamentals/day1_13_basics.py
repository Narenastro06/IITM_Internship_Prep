#Problem 10: A car travels 150km in 2.5 hours. Calculate speed in km/hr. Then convert it to m/s (divide by 3.6).
distance = 150 #In kilometers 
time = 2.5 #In hours
speed = distance/time
print("The speed of the car is: ",speed)
speed_in_meter_per_sec = speed/3.6
print("speed in meter per sec is: ",speed_in_meter_per_sec)